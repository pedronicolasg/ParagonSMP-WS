                  <p class="text-gray-700">
                    <b>Abuso de Bugs</b> <br>
                    Um bug geralmente é uma falha no servidor que se não for corrigido imediatamente, pode resultar em problemas graves no futuro. Utilizar esses bugs pode proporcionar vantagens injustas à determinados jogadores, o que não é permitido em nenhuma circunstância. Portanto, considera-se um ato de trapaça e resultará em banimento. Se encontrar algum bug, por favor, informe imediatamente à Staff para que seja solucionado o mais rápido possível.<br /><br />

                    <b>Anti-Jogo de modo Geral</b><br />
                    É considerado anti-jogo qualquer atitude que desestabilize a harmonia do servidor ou o convívio e relacionamento entre os jogadores. Uma prática de anti-jogo é utilizar de vantagens ilícitas para competir nas estatísticas (uso de contas fakes em eventos ou formação de times para auxiliar na vitória, por exemplo), etc. Tais condutas não são toleradas em nosso servidor e, se devidamente comprovadas, ocasionam a punição do seu responsável. Lembrando que nos casos de anti-jogo na competição de ranking das estatísticas, a reincidência acarretará reset de pontuação no devido ranking.<br /><br />

                    <b>Apologia / Racismo / Discriminação</b><br />
                    Discriminar ou inferiorizar raça, religião, condição econômica, orientação sexual, naturalidade ou o simples fato de incentivar, ou estimular tais preconceitos, além de crime, não é permitido em qualquer condição ou hipótese em nossos servidores. A restrição é ampliada para nosso Discord. As punições serão proporcionais aos danos morais causados, podendo ir de restrições de chat e chegando, em caso extremo, a banimento permanente do servidor.<br /><br />

                    <b>Chantagem ou Suborno</b><br />
                    Fica caracterizado chantagem ou suborno quando um jogador oferece à outro algo de seu interesse, com o objetivo de persuadi-lo a não revelar informações suspeitas ou inadequadas. Por exemplo, se um jogador descobre um bug no servidor e oferece recompensas à outro jogador para que este não reporte para a Staff. Se essa conduta for confirmada, ambos serão banidos.<br /><br />

                    <b>Cliente alternativo</b><br />
                    A utilização de clientes alternativos, hacks ou qualquer alteração nas configurações do Minecraft que confira vantagens exclusivas (seja em PvP, mineração, etc.) é considerada uma violação grave e não será tolerada em nossos servidores. O uso desses clientes alternativos tornam a jogabilidade desleal e os acusados serão banidos permanentemente.<br /><br />

                    <b>Comércio de contas, itens ou coins por dinheiro real</b><br />
                    Qualquer forma de comércio de contas em nosso Servidor e Discord, seja através de itens ou dinheiro real, é estritamente proibido. Esta regra se aplica a qualquer conta que seja vendida ou anunciada. É importante ressaltar que a aquisição de uma conta dessa maneira não confere propriedade sobre ela ao comprador, ou seja, o proprietário original da conta ainda possuirá o direito de recuperá-la no futuro usando os meios disponíveis para isso. Portanto, o comércio de contas, além de não autorizado pela Staff, não oferece garantias. Esta regra também se estende ao comércio de itens por dinheiro real.<br /><br />

                    <b>Construções Irregulares</b><br />
                    Construções que apresentem apelo sexual, gestos obscenos, racistas ou qualquer conteúdo inadequado são consideradas irregulares em nossos servidores. Além disso, qualquer construção realizada sem autorização, como em terrenos protegidos sem o consentimento do proprietário, também é considerada irregular.<br /><br />

                    <b>Conduta de Clans</b><br />
                    Qualquer clan que desrespeite algumas das regras aqui citadas pode e será sujeito à alertas consideráveis e penalizações em eventos grandes. Não serão deletados imediatamente, a menos que a maioria dos membros do clan participe da infração e ignore os alertas. Os membros infratores serão punidos conforme necessário, enquanto medidas disciplinares mais severas serão aplicadas caso persistam as violações.<br /><br />

                    <b>Conta Fake</b><br />
                    Qualquer jogador que crie uma conta fake para praticar uma atitude fora das regras aqui descritas será devidamente punido, tanto em sua conta original utilizada para jogar, quanto em sua conta fake utilizada para prejudicar outros jogadores e/ou o servidor.<br /><br />

                    <b>Desinformação com o Objetivo de FUD (Causar Medo, Incerteza e Dúvida)</b><br />
                    Qualquer jogador que disseminar informações falsas com a intenção de provocar medo, incerteza e dúvida entre outros jogadores, será punido de acordo com esta regra. Este tipo de comportamento prejudica a comunidade e compromete a integridade do Paragon SMP, portanto, medidas disciplinares severas serão aplicadas para preservar um ambiente justo e seguro para todos os jogadores.<br /><br />

                    <b>Divulgação de Servidores</b><br />
                    Qualquer forma de divulgação de servidores terceiros, mesmo caseiros, direta ou indiretamente, não será tolerada. Isso resultará em banimento de todas as contas envolvidas, sem possibilidade de revisão.<br /><br />

                    <b>Falsificação de provas</b><br />
                    A falsificação de provas não apenas viola as regras do servidor, mas também é considerada antiético. Todas as denúncias recebidas são analisadas detalhadamente e confrontadas com os registros do servidor (logs) para garantir sua veracidade e precisão. Caso seja identificado qualquer indício de um print ou vídeo falsificados com a intenção de criar evidências enganosas, as medidas punitivas serão revertidas contra o responsável pela falsificação.<br /><br />

                    <b>Flood</b><br />
                    A prática de flood é considerada poluição visual, portanto, é proibida. Qualquer tipo de mensagem repetitiva com o único objetivo de chamar atenção incide esta regra. Entendemos que alguns casos são acidentais. No entanto, o uso indiscriminado e frequente resultará em punição, como restrição de chat ou banimento temporário.<br /><br />

                    <b>Griefing</b><br />
                    Qualquer ato intencional de prejudicar outro jogador, seja colocando blocos para obstruir baús, portas, alçapões, ou utilizando lava/água para isso, é considerado uma infração grave. Os blocos serão identificados e removidos, e o jogador responsável será punido conforme esta regra. É importante ressaltar que, se o ato de griefing for identificado como sendo realizado por uma conta fake, ambas as contas serão punidas de acordo com a regra de Conta Fake.<br /><br />

                    <b>Nick Inadequado</b><br />
                    A criação de nicks contendo palavrões, ofensas, apologias ou qualquer nome com a intenção de denegrir um jogador, ou membro da Staff não será permitida. É importante escolher seu nick com cuidado para evitar punições desnecessárias. Vale ressaltar que, nesses casos, as regras para contas fake também serão aplicadas.<br /><br />

                    <b>Ofensa a Staff</b><br />
                    Ofensas a Staff não serão admitidas. Caso o jogador tenha algo para reclamar referente a conduta de qualquer membro da equipe, basta procurar os canais oficiais de relacionamento, como a ouvidoria, onde sua denúncia será analisada exclusivamente pelo DONO.<br /><br />

                    <b>Ofensa a Jogadores</b><br />
                    Qualquer forma de desrespeito ou ofensa direcionada a outros jogadores resultará em punição temporária ou restrição de chat. Esta regra é aplicável a todos os canais de comunicação, incluindo in-game e Discord. Se a ofensa for originada de uma conta fake, a punição será aplicada de acordo com a regra estabelecida para Contas Fake.<br /><br />

                    <b>Palavras Inadequadas</b><br />
                    Não será permitido em nosso servidor palavreado de baixo calão, como palavrões. Todo jogador que não respeitar os demais e utilizar esse tipo de palavreado será punido temporariamente ou sofrerá restrição de chat. Esta regra vale para todos os canais de comunicação, sejam eles in-game, Discord etc. Caso a ofensa seja dirigida para um jogador ou Staff, caberá a regra específica para esses casos, e se originada de uma conta fake, a punição poderá ser aplicada conforme a regra de Conta Fake.<br /><br />

                    <b>Perseguição / Cyberbullying</b><br />
                    Perseguição trata-se de ameaçar, assediar ou incomodar um jogador repetidamente, isso com a intenção de causar constrangimento, medo ou qualquer tipo de perturbação psicológica. Isso é entendido como Cyberbullying, que além de crime é estritamente proibido em nosso Servidor e Discord. Um exemplo disso é a divulgação de dados pessoais com intuito de constranger alguém. Se constatado pela equipe da Staff a infração desta regra, o infrator será devidamente punido.<br /><br />

                    <b>Representação</b><br />
                    É proibido o uso de contas com nick parecido com membros da Staff, cujo intuito seja de causar confusão e se passar pelo mesmo. Tal comportamento é estritamente proibido.<br /><br />

                    <b>Skin Inadequada</b><br />
                    Skins com simbologia nazista/racista, representações do corpo humano nu ou imagens/desenhos obscenos serão banidas para garantir um ambiente respeitoso e livre de conteúdo inapropriado para todos os jogadores. Jogadores que estiverem utilizando skins desta magnitude terão suas contas banidas do servidor, e, além disso, a skin vinculada a conta será bloqueada.<br /><br />
